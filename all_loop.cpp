#include <iostream>
using namespace std;
int main()
{

    for (int i = 1; i <= 10; i++)
    {
        cout << i << " ";
    }
    cout << endl;

    
    
    
    
    // while loop
    int j = 10;
    while (j >= 1)
    {
        cout << j << " ";
        j--;
    }
    cout << endl;

    
    
    
    
    // do while loop
    int k = 1;
    do
    {
        cout << k << " ";
        k++;

    } while (k <= 10);
    cout << endl;

    return 0;
}